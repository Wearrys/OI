class ResistorFactory {

    public:
    vector<int> construct(long long nanoOhms) {
        int tot = 0;
        vector<int> opt;

        function<int(long long, bool, int)> solve = [&] (long long x, bool ty, int atom) {
            if(x == 1) 
                return atom;
            else if(x & 1) {
                int y = solve(x - 1, ty, atom);
                opt.push_back(y);
                opt.push_back(atom);
                opt.push_back(ty);
                return ++ tot;
            } else {
                int y = solve(x / 2, ty, atom);
                opt.push_back(y);
                opt.push_back(y);
                opt.push_back(ty);
                return ++ tot;
            }
        };

        solve(nanoOhms, 0, solve(1e9, 1, 0));

        return opt;
    }
};

